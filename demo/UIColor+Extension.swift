import UIKit

extension UIColor {
    static var earth: UIColor {
        return UIColor(red: 0.34, green: 0.13, blue: 0.55, alpha: 1)
    }
    
    static var mars: UIColor {
        return UIColor(red: 0.87, green: 0.12, blue: 0.11, alpha: 1)
    }
    
    static var venus: UIColor {
        return UIColor(red: 0.77, green: 0.22, blue: 0.15, alpha: 1)
    }
}
